<script setup>
import { onMounted } from 'vue'

onMounted(() => {
  initCalendar()
})

const initCalendar = () => {
  const monthNames = [
    'Enero', 'Febrero', 'Marzo', 'Abril', 'Mayo', 'Junio',
    'Julio', 'Agosto', 'Septiembre', 'Octubre', 'Noviembre', 'Diciembre'
  ]

  let currentMonth = new Date().getMonth()
  let currentYear = new Date().getFullYear()

  const updateCalendar = () => {
    const monthElement = document.getElementById('currentMonth')
    const calendarElement = document.getElementById('calendar')

    if (!monthElement || !calendarElement) return

    monthElement.textContent = `${monthNames[currentMonth]} ${currentYear}`

    // Clear calendar
    calendarElement.innerHTML = ''

    // Days of week header
    const daysOfWeek = ['Dom', 'Lun', 'Mar', 'Mié', 'Jue', 'Vie', 'Sáb']
    daysOfWeek.forEach(day => {
      const dayElement = document.createElement('div')
      dayElement.classList.add('calendar-day-header')
      dayElement.textContent = day
      calendarElement.appendChild(dayElement)
    })

    // Get first day of month and number of days
    const firstDay = new Date(currentYear, currentMonth, 1).getDay()
    const daysInMonth = new Date(currentYear, currentMonth + 1, 0).getDate()

    // Add empty cells for days before first day of month
    for (let i = 0; i < firstDay; i++) {
      const emptyDay = document.createElement('div')
      emptyDay.classList.add('calendar-day', 'empty')
      calendarElement.appendChild(emptyDay)
    }

    // Add days of month
    for (let day = 1; day <= daysInMonth; day++) {
      const dayElement = document.createElement('div')
      dayElement.classList.add('calendar-day')
      dayElement.textContent = day

      // Add random emblema to some days (this is just for demonstration)
      if (Math.random() > 0.8) {
        const emblema = document.createElement('div')
        emblema.classList.add('day-emblema')
        emblema.innerHTML = '🌙' // This could be different emblemas
        dayElement.appendChild(emblema)
      }

      calendarElement.appendChild(dayElement)
    }
  }

  // Navigation buttons
  const prevButton = document.getElementById('prevMonth')
  const nextButton = document.getElementById('nextMonth')

  if (prevButton) {
    prevButton.addEventListener('click', () => {
      currentMonth--
      if (currentMonth < 0) {
        currentMonth = 11
        currentYear--
      }
      updateCalendar()
    })
  }

  if (nextButton) {
    nextButton.addEventListener('click', () => {
      currentMonth++
      if (currentMonth > 11) {
        currentMonth = 0
        currentYear++
      }
      updateCalendar()
    })
  }

  updateCalendar()
}
</script>

